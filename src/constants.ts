适用：日常对话、行走交谈、轻度动作
标准中景
用法：人物腰部以上
情绪：平和叙事、平等对话、情绪克制
适用：双人对白、日常互动、剧情推进
中近景
用法：胸口以上，弱化环境、突出人物神态
